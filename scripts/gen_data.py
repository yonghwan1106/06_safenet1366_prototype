def main():
    BASE = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    sigungu_list = []
    for sido_code, sido_name, items in SIGUNGU_DATA:
        for code, name, pop in items:
            sigungu_list.append({
                'code': code, 'sido': sido_name, 'sidoCode': sido_code,
                'name': name, 'population': pop,
                'households': int(pop / 2.4),
            })

    risk_list = []
    for sg in sigungu_list:
        cd = round(random.uniform(0.2, 0.95), 3)
        rr = round(random.uniform(0.15, 0.55), 3)
        sa = round(random.uniform(0.05, 0.85), 3)
        mw = round(random.uniform(1.0, 1.4), 2)
        cw = round(random.uniform(1.0, 1.35), 2)
        # 산식 그대로
        score = (0.4*cd + 0.3*rr) * (1-sa) * (1 + (mw-1) + (cw-1)) * 100
        score = max(0, min(100, round(score, 1)))
        trend = random.choice(['up','flat','down'])
        risk_list.append({
            'sigunguCode': sg['code'],
            'score': score,
            'components': {
                'counselDensity': cd, 'recallRate': rr,
                'shelterAvailability': sa,
                'multicultureWeight': mw, 'childcareWeight': cw,
            },
            'trend': trend,
            'updatedAt': '2026-05-03T00:00:00Z',
        })
    risk_list.sort(key=lambda x: -x['score'])
    for i, r in enumerate(risk_list, 1):
        r['rank'] = i

    # 보호시설: 시군구당 0~2개 무작위
    shelters = []
    types = ['emergency','short','long']
    sid = 1
    for sg in sigungu_list:
        n = random.choices([0,1,2,3], weights=[2,4,2,1])[0]
        for _ in range(n):
            cap = random.choice([10, 15, 20, 30, 40])
            occ = int(cap * random.uniform(0.3, 0.95))
            shelters.append({
                'id': f's{sid:03d}',
                'name': f"{sg['name']} {random.choice(['여성위기지원센터','가정폭력 상담소','보호쉼터','긴급피난처'])}",
                'sigunguCode': sg['code'],
                'lat': round(35 + random.uniform(0, 5), 4),
                'lng': round(126 + random.uniform(0, 4), 4),
                'capacity': cap, 'occupied': occ,
                'type': random.choice(types),
                'multilingual': random.random() < 0.35,
                'phone': f"{sg['sidoCode']}-XXXX-{random.randint(1000,9999)}",
            })
            sid += 1

    with open(os.path.join(BASE, 'data/sigungu.json'), 'w', encoding='utf-8') as f:
        json.dump(sigungu_list, f, ensure_ascii=False, indent=1)
    with open(os.path.join(BASE, 'data/risk-index.json'), 'w', encoding='utf-8') as f:
        json.dump(risk_list, f, ensure_ascii=False, indent=1)
    with open(os.path.join(BASE, 'data/shelters.json'), 'w', encoding='utf-8') as f:
        json.dump(shelters, f, ensure_ascii=False, indent=1)
    print(f'OK sigungu={len(sigungu_list)} risk={len(risk_list)} shelters={len(shelters)}')

if __name__ == '__main__':
    main()
